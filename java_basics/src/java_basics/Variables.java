package java_basics;

public class Variables {

	public static void main(String[] args) {
		
		int x; // Declaring a variable x variable as an integer
		x = 34; // Assigns 34 for variable x.
		
		System.out.println("x = " + x); // Prints the x variable to the screen;
		
		x = 23; // Changes x variable to 23. 
		
		System.out.println("x has been changed. x = " + x); // Prints x variable.
		System.out.println("Adding 4 to x = " + (x+4)); // Prints x after adding 4.
		
		
		String words; // Declaring a variable words as an String Data type.
		
		words = "This is a sentance"; // assigns a string to word variable.
		
		System.out.println("words = " + words); // Prints String stored in words variable. 
		
	}

}
